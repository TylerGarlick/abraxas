#!/usr/bin/env node
/**
 * sync-constitutions.js
 * 
 * Automatically syncs individual constitution files into constitution-all.md
 * and updates genesis.md with current system roster.
 * 
 * Usage:
 *   node scripts/sync-constitutions.js
 *   node scripts/sync-constitutions.js --validate
 *   node scripts/sync-constitutions.js --changelog
 */

const fs = require('fs');
const path = require('path');

const CONSTITUTION_DIR = path.join(__dirname, '..', 'constitution');
const GENESIS_PATH = path.join(CONSTITUTION_DIR, 'genesis.md');
const ALL_PATH = path.join(CONSTITUTION_DIR, 'constitution-all.md');
const INDEX_PATH = path.join(CONSTITUTION_DIR, 'constitution-index.md');

/**
 * Get all constitution files (excluding combined files)
 */
function getConstitutionFiles() {
  const files = fs.readdirSync(CONSTITUTION_DIR);
  return files
    .filter(f => f.endsWith('.md'))
    .filter(f => f !== 'constitution-all.md')
    .filter(f => f !== 'constitution-index.md')
    .filter(f => f !== 'genesis.md')
    .sort();
}

/**
 * Read constitution file and extract metadata
 */
function readConstitution(filename) {
  const filepath = path.join(CONSTITUTION_DIR, filename);
  const content = fs.readFileSync(filepath, 'utf8');
  
  // Extract system name from first heading
  const nameMatch = content.match(/^##?\s*(.+?)\s*$/m);
  const name = nameMatch ? nameMatch[1].replace('Constitution', '').trim() : filename.replace('.md', '');
  
  // Extract commands table
  const commandsMatch = content.match(/\| Command \| Function \|(?:[\s\S]*?)\n\n/);
  const commandsSection = commandsMatch ? commandsMatch[0] : null;
  
  // Extract command count
  const commandRows = content.match(/\| `\/[^`]+` \|/g);
  const commandCount = commandRows ? commandRows.length : 0;
  
  return {
    filename,
    filepath,
    name,
    content,
    commandCount,
    commandsSection
  };
}

/**
 * Generate constitution-all.md from individual files
 */
function generateAllConstitution(constitutions) {
  const header = `# constitution-all.md
## Full Abraxas Constitution — All Systems

---

> **For the human reading this:**
>
> This is the complete Abraxas constitution with all systems.
> Auto-generated by sync-constitutions.js from individual constitution files.
>
> **Systems in this fragment:**
${constitutions.map(c => `> - ${c.name} (${c.commandCount} commands)`).join('\n')}
>
> **Total commands:** ${constitutions.reduce((sum, c) => sum + c.commandCount, 0)}

---

`;

  const body = constitutions.map(c => {
    return `## ${c.name}\n\n${c.content}\n\n---\n\n`;
  }).join('');

  return header + body;
}

/**
 * Update genesis.md with current system roster
 */
function updateGenesis(constitutions) {
  const genesis = fs.readFileSync(GENESIS_PATH, 'utf8');
  
  // Update system list in Quick Start section (use clean names, not filenames)
  const systemNames = constitutions
    .map(c => c.name.split(' ')[0])  // Get first word of name
    .filter(n => n && n !== 'Constitution')  // Filter out generic names
    .sort()
    .join(', ');
  
  let updated = genesis.replace(
    /(Initialize all systems \()([^)]+)(\))/,
    `($1${systemNames}$3`
  );
  
  // Get unique commands only (no duplicates)
  const allCommands = new Set();
  constitutions.forEach(c => {
    const cmds = c.content.match(/`\/[^`]+`/g);
    if (cmds) {
      cmds.forEach(cmd => allCommands.add(cmd));
    }
  });
  
  // Only include first 20 unique commands in genesis (keep it readable)
  const commandList = Array.from(allCommands).slice(0, 20).join(' | ');
  
  if (commandList) {
    updated = updated.replace(
      /(## Core Commands\n)([^\n]+)/,
      `$1${commandList}`
    );
  }
  
  return updated;
}

/**
 * Generate constitution index
 */
function generateIndex(constitutions) {
  const header = `# Constitution Index

**Auto-generated:** ${new Date().toISOString()}
**Total Systems:** ${constitutions.length}
**Total Commands:** ${constitutions.reduce((sum, c) => sum + c.commandCount, 0)}

---

## System Roster

| System | File | Commands | Description |
|--------|------|----------|-------------|
${constitutions.map(c => `| ${c.name} | ${c.filename} | ${c.commandCount} | ${c.name} constitution |`).join('\n')}

---

## Quick Links

- [Full Constitution](constitution-all.md) — All systems combined
- [Genesis](genesis.md) — Initialization instructions
- [Individual Systems](./) — System-specific constitutions

---

## Sync Status

Last synced: ${new Date().toISOString()}
Sync script: scripts/sync-constitutions.js

`;

  return header;
}

/**
 * Validate constitutions for structural issues
 */
function validateConstitutions(constitutions) {
  const errors = [];
  const warnings = [];
  
  constitutions.forEach(c => {
    // Check for required sections
    if (!c.content.includes('Commands')) {
      warnings.push(`${c.filename}: No commands section found`);
    }
    
    // Check for Universal Constraints reference
    if (!c.content.includes('Universal Constraints') && c.name !== 'Universal') {
      // Not all systems need this
    }
    
    // Check for command table format
    if (!c.content.match(/\| Command \| Function \|/)) {
      warnings.push(`${c.filename}: Command table may be malformed`);
    }
  });
  
  // Check for duplicate commands
  const allCommands = [];
  constitutions.forEach(c => {
    const cmds = c.content.match(/`\/[^`]+`/g);
    if (cmds) {
      cmds.forEach(cmd => {
        if (allCommands.includes(cmd)) {
          errors.push(`Duplicate command: ${cmd}`);
        }
        allCommands.push(cmd);
      });
    }
  });
  
  return { errors, warnings, allCommands: allCommands.length };
}

/**
 * Generate changelog
 */
function generateChangelog(constitutions) {
  const stats = fs.statSync(ALL_PATH);
  
  console.log('Constitution Changelog');
  console.log('='.repeat(60));
  console.log(`Last Modified: ${stats.mtime.toISOString()}`);
  console.log(`Total Systems: ${constitutions.length}`);
  console.log(`Total Commands: ${constitutions.reduce((sum, c) => sum + c.commandCount, 0)}`);
  console.log('');
  console.log('Systems:');
  constitutions.forEach(c => {
    const fileStats = fs.statSync(c.filepath);
    console.log(`  ${c.name}: ${c.commandCount} commands (modified: ${fileStats.mtime.toISOString()})`);
  });
}

// Main execution
if (require.main === module) {
  const args = process.argv.slice(2);
  
  console.log('Syncing Constitutions...');
  console.log('='.repeat(60));
  
  const constitutions = getConstitutionFiles().map(f => readConstitution(f));
  
  console.log(`Found ${constitutions.length} constitution files:`);
  constitutions.forEach(c => {
    console.log(`  - ${c.filename} (${c.commandCount} commands)`);
  });
  console.log('');
  
  if (args.includes('--validate')) {
    console.log('Validating Constitutions...');
    const validation = validateConstitutions(constitutions);
    
    if (validation.errors.length > 0) {
      console.log('ERRORS:');
      validation.errors.forEach(e => console.log(`  ❌ ${e}`));
    }
    
    if (validation.warnings.length > 0) {
      console.log('WARNINGS:');
      validation.warnings.forEach(w => console.log(`  ⚠️  ${w}`));
    }
    
    console.log(`Total unique commands: ${validation.allCommands}`);
    console.log('');
  }
  
  // Generate constitution-all.md
  console.log('Generating constitution-all.md...');
  const allContent = generateAllConstitution(constitutions);
  fs.writeFileSync(ALL_PATH, allContent);
  console.log(`  ✓ Written to ${ALL_PATH}`);
  
  // Update genesis.md
  console.log('Updating genesis.md...');
  const genesisContent = updateGenesis(constitutions);
  fs.writeFileSync(GENESIS_PATH, genesisContent);
  console.log(`  ✓ Updated ${GENESIS_PATH}`);
  
  // Generate index
  console.log('Generating constitution-index.md...');
  const indexContent = generateIndex(constitutions);
  fs.writeFileSync(INDEX_PATH, indexContent);
  console.log(`  ✓ Written to ${INDEX_PATH}`);
  
  if (args.includes('--changelog')) {
    console.log('');
    generateChangelog(constitutions);
  }
  
  console.log('');
  console.log('='.repeat(60));
  console.log('Sync complete!');
  console.log('');
  console.log('Next steps:');
  console.log('  git add constitution/');
  console.log('  git commit -m "Sync constitutions: ' + constitutions.length + ' systems, ' + constitutions.reduce((sum, c) => sum + c.commandCount, 0) + ' commands"');
  console.log('  git push origin main');
}

module.exports = {
  getConstitutionFiles,
  readConstitution,
  generateAllConstitution,
  updateGenesis,
  generateIndex,
  validateConstitutions
};
